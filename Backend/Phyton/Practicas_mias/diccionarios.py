#Los diccionarios se utilizan para almacenar valores de datos en pares clave:valor.
#Un diccionario es una colección ordenada*, modificable y que no admite duplicados.
# creacion de un diccionario
diccionario = {}

diccionario['nombre'] = 'Pedro'
diccionario['edad'] = 35
diccionario['pais'] = 'Colombia'
diccionario['profesion'] = 'Programador'

print(diccionario)
#imprimir tamaño de un diccionario
print(f'imprimir el tamaño del diccionario  {len(diccionario)}')




